using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class SoundManager : StateMachineBehaviour
{
    readonly string[] states = {"Jumping", "Attacking", "Repairing"};
    
    // OnStateEnter is called when a transition starts and the state machine starts to evaluate this state
    override public void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        AudioSource audioSource = animator.gameObject.GetComponent<AudioSource>();
        foreach (string state in states)
        {
            if (stateInfo.IsName(state))
            {
                AudioClip clip = Resources.Load("Sounds/" + state) as AudioClip;
                audioSource.PlayOneShot(clip);
            }
        }
    }

